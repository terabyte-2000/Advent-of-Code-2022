function overlapMany(a,b){
    const res = [];
    for(let ca of a){
        for(let cb of b){
            if(ca === cb) res.push(ca);
        }
    }

    return res;
}

function threeOverlap(a,b,c){
    return overlap(overlapMany(a,b).join(''), overlapMany(b,c).join(''));
}

